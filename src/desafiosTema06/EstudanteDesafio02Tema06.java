package desafiosTema06;

public class EstudanteDesafio02Tema06 {

    int id;
    String nome;
    double nota;

    public EstudanteDesafio02Tema06(int id, String nome, double nota) {
        this.id = id;
        this.nome = nome;
        this.nota = nota;
    }

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public double getNota() {
        return nota;
    }
}
